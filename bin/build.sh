#!/usr/bin/env bash
# build.sh -- concatenate lib/*.bash into new.bashrc.
#
# Source of truth for v2 is lib/[0-9][0-9]-*.bash. The single-file new.bashrc is
# the build artifact users install (cp new.bashrc ~/.bashrc). CI verifies that
# new.bashrc matches a fresh build (`make check`).
#
# Run via `make build` from the project root.

set -euo pipefail

root="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
lib_dir="$root/lib"
out="$root/new.bashrc"

if [ ! -d "$lib_dir" ]; then
    echo "build.sh: $lib_dir not found" >&2
    exit 1
fi

# Collect the modules in lexical (= numeric prefix) order. Glob expands to a
# stable, sorted list because bash sorts shell globs by default.
shopt -s nullglob
modules=( "$lib_dir"/[0-9][0-9]-*.bash )
shopt -u nullglob

if [ "${#modules[@]}" -eq 0 ]; then
    echo "build.sh: no lib/[0-9][0-9]-*.bash modules found" >&2
    exit 1
fi

tmp="$(mktemp "${TMPDIR:-/tmp}/new.bashrc.XXXXXX")"
trap 'rm -f "$tmp"' EXIT

{
    # shellcheck disable=SC2016
    printf '%s\n' '# Generated by bin/build.sh -- do not edit. Edit lib/*.bash and run `make build`.'
    printf '%s\n' '# shellcheck shell=bash'
    for f in "${modules[@]}"; do
        cat -- "$f"
    done
} > "$tmp"

mv -- "$tmp" "$out"
trap - EXIT
printf 'wrote %s (%d modules)\n' "$out" "${#modules[@]}"
